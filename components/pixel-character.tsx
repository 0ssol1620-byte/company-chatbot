const CHARACTER_DATA: Record<AvatarType, string[][]> = {
  warrior,
  mage,
  archer,
  healer,
  rogue,
  bard,
}

interface PixelCharacterProps {
  type: AvatarType
  size?: number
  animated?: boolean
  isThinking?: boolean
}

export function PixelCharacter({ type, size = 64, animated = false, isThinking = false }: PixelCharacterProps) {
  const grid = CHARACTER_DATA[type]
  const rows = grid.length
  const cols = grid[0].length
  const pixelSize = size / Math.max(rows, cols)

  return (
    <div className="relative inline-block" style={{ width: size, height: size }}>
      {isThinking && (
        <div
          className="absolute -top-6 left-1/2 -translate-x-1/2 flex gap-1 items-end"
          style={{ fontFamily: "'Press Start 2P', monospace", fontSize: 6 }}
        >
          <div className="bg-[#1a1a1a] border border-white rounded px-1.5 py-0.5 text-white whitespace-nowrap flex gap-0.5">
            <span className="inline-block animate-bounce" style={{ animationDelay: '0ms', animationDuration: '0.6s' }}>.</span>
            <span className="inline-block animate-bounce" style={{ animationDelay: '200ms', animationDuration: '0.6s' }}>.</span>
            <span className="inline-block animate-bounce" style={{ animationDelay: '400ms', animationDuration: '0.6s' }}>.</span>
          </div>
        </div>
      )}
      <svg
        width={size}
        height={size}
        viewBox={`0 0 ${cols} ${rows}`}
        className={animated ? 'animate-breathe' : ''}
        style={{ imageRendering: 'pixelated' }}
      >
        {grid.map((row, y) =>
          row.map((color, x) =>
            color ? (
              <rect key={`${x}-${y}`} x={x} y={y} width={1} height={1} fill={color} />
            ) : null
          )
        )}
      </svg>
    </div>
  )
}
